try:
    # 1. Программа запрашивает общее количество произведенных капсул (целое число).
    total_capsules = int(input("Введите общее количество произведенных капсул: "))

    # 2. Запрашивает вместимость одной упаковки (например, 30 шт).
    capacity = int(input("Введите количество капсул в одной упаковке: "))

    # Проверка на ввод нуля для вместимости
    if capacity == 0:
        print("Вместимость упаковки не может быть нулем.")
    else:
        # 3. Используя операторы // и %, выведите:
        #    * Сколько полных упаковок получится.
        full_packs = total_capsules // capacity
        #    * Сколько капсул останется (не поместятся в полную упаковку).
        remaining_capsules = total_capsules % capacity

        print("--- Отчет фасовочного цеха ---")
        print(f"Полных упаковок: {full_packs}")
        print(f"Остаток капсул: {remaining_capsules}")

except ValueError:
    print("Пожалуйста, введите целые числа.")