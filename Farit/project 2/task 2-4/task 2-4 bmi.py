# Получение веса и роста от пользователя
weight_kg = float(input("Введите ваш вес (кг): "))
height_cm = float(input("Введите ваш рост (см): "))

# Преобразование роста из см в м
height_m = height_cm / 100

# Расчет индекса массы тела
bmi = weight_kg / (height_m ** 2)

# Форматирование и вывод результата
print("\n--- Отчет о состоянии здоровья ---")
print(f"Рост:\t{height_cm:.1f} см")
print(f"Вес:\t{weight_kg:.1f} кг")
print(f"Индекс массы тела: {bmi:.2f}")